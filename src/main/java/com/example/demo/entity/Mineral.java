package com.example.demo.entity;

public class Mineral {

    private int id;

    private String name;

    private String type;

    private String introduction;

    private String color;

    private String jieli;

    private String tuqi;

    private String colorganshe;

    private int xiaoguang;

    private int shuangjin;

    private int guangxing;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getIntroduction() {
        return introduction;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getJieli() {
        return jieli;
    }

    public void setJieli(String jieli) {
        this.jieli = jieli;
    }

    public String getTuqi() {
        return tuqi;
    }

    public void setTuqi(String tuqi) {
        this.tuqi = tuqi;
    }

    public String getColorganshe() {
        return colorganshe;
    }

    public void setColorganshe(String colorganshe) {
        this.colorganshe = colorganshe;
    }

    public int getXiaoguang() {
        return xiaoguang;
    }

    public void setXiaoguang(int xiaoguang) {
        this.xiaoguang = xiaoguang;
    }

    public int getShuangjin() {
        return shuangjin;
    }

    public void setShuangjin(int shuangjin) {
        this.shuangjin = shuangjin;
    }

    public int getGuangxing() {
        return guangxing;
    }

    public void setGuangxing(int guangxing) {
        this.guangxing = guangxing;
    }
}
