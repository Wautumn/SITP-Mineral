public class NewPic {
    private int id;

    private String name;

    private String picdan;

    private String picshi;

    private String picyun;

    private String piczheng;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPicdan() {
        return picdan;
    }

    public void setPicdan(String picdan) {
        this.picdan = picdan;
    }

    public String getPicshi() {
        return picshi;
    }

    public void setPicshi(String picshi) {
        this.picshi = picshi;
    }

    public String getPicyun() {
        return picyun;
    }

    public void setPicyun(String picyun) {
        this.picyun = picyun;
    }

    public String getPiczheng() {
        return piczheng;
    }

    public void setPiczheng(String piczheng) {
        this.piczheng = piczheng;
    }
}
